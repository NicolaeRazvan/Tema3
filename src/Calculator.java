public class Calculator {

    public  int suma(int a, int b){
        return a + b;

    }

    public  int impartire(int a, int b){
        return a / b;
    }

    public  int calcul1(int a, int b, int c){
        return -a + b * c;
    }

    public  int calcul2(int a,int b ,int c){
        return (a + b) % c;
    }

    public  int calcul3(int a , int b , int c ,int d){
        return a + (-b) * c / d;
    }

    public int calcul4(int a , int b , int c ,int d , int e , int f){
        return a + b / c * d - e % f;
    }
}
